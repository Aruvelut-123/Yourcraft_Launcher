using System;
using System.Collections.Generic;
using System.Linq;
using System.Security.Policy;
using System.Security.RightsManagement;
using System.Text;
using System.Threading.Tasks;
using System.Xml;
using static DeCraftLauncher.JarUtils;

namespace DeCraftLauncher.Configs
{

    public class JarConfig
    {
        public string friendlyName = "";
        public string jarFileName;
        public List<EntryPoint> entryPoints = new List<EntryPoint>();
        public string LWJGLVersion = "2.9.3";
        public string playerName = "DECRAFT_Player";
        public bool entryPointsScanned = false;
        public string instanceDirName = "";
        public string jvmArgs = "-Djava.util.Arrays.useLegacyMergeSort=true";
        public string proxyHost = "";
        public uint windowW = 960;
        public uint windowH = 540;
        public string maxJavaVersion = "";
        public string minJavaVersion = "";

        //advanced options
        public string sessionID = "0";
        public string gameArgs = "";
        public bool cwdIsDotMinecraft = false;
        public bool appletEmulateHTTP = true;
        public string documentBaseUrl = "http://www.minecraft.net/play.jsp";
        public bool appletRedirectSkins = true;
        public string appletSkinRedirectPath = "C:\\skincache";
        public bool jarHasLWJGLClasses = false;
        public string jarBuiltInLWJGLDLLs = "";

        public JarConfig(string jarFileName)
        {
            this.jarFileName = jarFileName;
            instanceDirName = jarFileName;
        }

        private XmlNode SetVal(XmlNode a, string v)
        {
            a.InnerText = v;
            return a;
        }

        public void SaveToXML(string path)
        {
            XmlDocument newXml = new XmlDocument();
            newXml.LoadXml("<?xml version=\"1.0\"?>\n<JarConfig>\n</JarConfig>");
            XmlNode rootElement = newXml.GetElementsByTagName("JarConfig")[0];
            rootElement.AppendChild(Utils.GenElementChild(newXml, "FriendlyName", friendlyName));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "LWJGLVersion", LWJGLVersion));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "PlayerName", playerName));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "InstanceDirectory", instanceDirName));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "JVMArgs", jvmArgs));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "WindowW", windowW.ToString()));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "WindowH", windowH + ""));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "ProxyHost", proxyHost));

            //advanced options
            rootElement.AppendChild(Utils.GenElementChild(newXml, "PassSessionID", sessionID));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "GameArgs", gameArgs));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "JavaCwdIsDotMinecraft", cwdIsDotMinecraft.ToString()));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "AppletEmulateHTTP", appletEmulateHTTP.ToString()));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "AppletDocumentURL", documentBaseUrl));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "AppletRedirectToLocalSkins", appletRedirectSkins.ToString()));
            rootElement.AppendChild(Utils.GenElementChild(newXml, "AppletSkinRedirectLocalPath", appletSkinRedirectPath));


            //generated by jar scanner
            XmlNode nodeValsAutogenerated = rootElement.AppendChild(Utils.GenElementChild(newXml, "AutoGenerated"));
            nodeValsAutogenerated.AppendChild(Utils.GenElementChild(newXml, "EntryPointsScanned", entryPointsScanned.ToString()));
            nodeValsAutogenerated.AppendChild(Utils.GenElementChild(newXml, "MaxJavaVersion", maxJavaVersion));
            nodeValsAutogenerated.AppendChild(Utils.GenElementChild(newXml, "MinJavaVersion", minJavaVersion));
            nodeValsAutogenerated.AppendChild(Utils.GenElementChild(newXml, "JarHasLWJGL", jarHasLWJGLClasses.ToString()));
            nodeValsAutogenerated.AppendChild(Utils.GenElementChild(newXml, "JarLWJGLNativesDir", jarBuiltInLWJGLDLLs));

            XmlNode entryPointsList = nodeValsAutogenerated.AppendChild(Utils.GenElementChild(newXml, "EntryPoints"));
            foreach (EntryPoint a in entryPoints)
            {
                XmlNode nEntryPoint = entryPointsList.AppendChild(Utils.GenElementChild(newXml, "EntryPoint"));
                nEntryPoint.AppendChild(Utils.GenElementChild(newXml, "ClassPath", a.classpath));
                nEntryPoint.AppendChild(Utils.GenElementChild(newXml, "LaunchType", ((int)a.type).ToString()));
                nEntryPoint.AppendChild(Utils.GenElementChild(newXml, "ClassInfo", a.additionalInfo));
            }

            newXml.Save(path);
        }

        public static JarConfig LoadFromXML(string path, string jarName)
        {
            JarConfig newJarConf = new JarConfig(jarName);

            XmlDocument newXml = new XmlDocument();
            newXml.Load(path);
            XmlNode rootNode = newXml.SelectSingleNode("JarConfig");
            if (rootNode != null)
            {

                newJarConf.friendlyName = Utils.GetInnerOrDefault(rootNode, "FriendlyName");
                newJarConf.LWJGLVersion = Utils.GetInnerOrDefault(rootNode, "LWJGLVersion", "2.9.3");
                newJarConf.playerName = Utils.GetInnerOrDefault(rootNode, "PlayerName", "DECRAFT_player");
                newJarConf.jvmArgs = Utils.GetInnerOrDefault(rootNode, "JVMArgs", "-Djava.util.Arrays.useLegacyMergeSort=true");
                newJarConf.instanceDirName = Utils.GetInnerOrDefault(rootNode, "InstanceDirectory", jarName);
                newJarConf.windowW = uint.Parse(Utils.GetInnerOrDefault(rootNode, "WindowW", "960", "uint"));
                newJarConf.windowH = uint.Parse(Utils.GetInnerOrDefault(rootNode, "WindowH", "540", "uint"));
                newJarConf.proxyHost = Utils.GetInnerOrDefault(rootNode, "ProxyHost");

                newJarConf.sessionID = Utils.GetInnerOrDefault(rootNode, "SessionID", "0");
                newJarConf.gameArgs = Utils.GetInnerOrDefault(rootNode, "GameArgs");
                newJarConf.cwdIsDotMinecraft = bool.Parse(Utils.GetInnerOrDefault(rootNode, "JavaCwdIsDotMinecraft", "true", "bool"));
                newJarConf.appletEmulateHTTP = bool.Parse(Utils.GetInnerOrDefault(rootNode, "AppletEmulateHTTP", "true", "bool"));
                newJarConf.documentBaseUrl = Utils.GetInnerOrDefault(rootNode, "AppletDocumentURL", "http://www.minecraft.net/play.jsp");
                newJarConf.appletRedirectSkins = bool.Parse(Utils.GetInnerOrDefault(rootNode, "AppletRedirectToLocalSkins", "true", "bool"));
                newJarConf.appletSkinRedirectPath = Utils.GetInnerOrDefault(rootNode, "AppletSkinRedirectLocalPath", "C:\\skincache");

                //generated by jar scanner
                XmlNode nodeValsAutogenerated = rootNode.SelectSingleNode("AutoGenerated");
                if (nodeValsAutogenerated != null)
                {
                    newJarConf.entryPointsScanned = bool.Parse(Utils.GetInnerOrDefault(nodeValsAutogenerated, "EntryPointsScanned", "false", "bool"));
                    newJarConf.maxJavaVersion = Utils.GetInnerOrDefault(nodeValsAutogenerated, "MaxJavaVersion");
                    newJarConf.minJavaVersion = Utils.GetInnerOrDefault(nodeValsAutogenerated, "MinJavaVersion");
                    newJarConf.jarHasLWJGLClasses = bool.Parse(Utils.GetInnerOrDefault(nodeValsAutogenerated, "JarHasLWJGL", "false", "bool"));
                    newJarConf.jarBuiltInLWJGLDLLs = Utils.GetInnerOrDefault(nodeValsAutogenerated, "JarLWJGLNativesDir");
                }
                foreach (XmlNode a in newXml.GetElementsByTagName("EntryPoint"))
                {
                    EntryPoint b = new EntryPoint();
                    string classPath = Utils.GetInnerOrDefault(a, "ClassPath", null);
                    string type = Utils.GetInnerOrDefault(a, "LaunchType", null, "int");
                    string additionalInfo = Utils.GetInnerOrDefault(a, "ClassInfo", "");
                    if (classPath == null || type == null)
                    {
                        continue;
                    }
                    b.classpath = classPath;
                    try
                    {
                        b.type = (EntryPointType)int.Parse(type);
                    }
                    catch (FormatException)
                    {
                        continue;
                    }
                    b.additionalInfo = additionalInfo;
                    newJarConf.entryPoints.Add(b);
                }
            }

            return newJarConf;
        }
    }
}
